package org.apache.spark.api.python;
/**
 * A wrapper for both GatewayServer, and ClientServer to pin Python thread to JVM thread.
 */
  class Py4JServer implements org.apache.spark.internal.Logging {
  public   Py4JServer (org.apache.spark.SparkConf sparkConf)  { throw new RuntimeException(); }
  public  int getListeningPort ()  { throw new RuntimeException(); }
    java.lang.String secret ()  { throw new RuntimeException(); }
    java.lang.Object server ()  { throw new RuntimeException(); }
  public  void shutdown ()  { throw new RuntimeException(); }
  public  void start ()  { throw new RuntimeException(); }
}
